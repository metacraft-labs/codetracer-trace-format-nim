## Tests for ct-print --full / --events output.
##
## Strategy:
##   1. Build a representative .ct fixture in-process (same content as
##      tests/generate_ct_print_fixture.nim, but inline so the test can
##      run hermetically without a separate binary).
##   2. Read the fixture back via the same NewTraceReader path that ct-print
##      uses, and call buildFullDocument(). This validates the conversion
##      logic without shelling out.
##   3. Walk the resulting JSON and assert: variable values are present,
##      decoded with the right structure (Int -> i, String -> text, etc.),
##      call args/return values are decoded, IO bytes appear as base64,
##      and the events list is in deterministic order.
##   4. Snapshot test: dump the JSON, compare against a committed golden
##      file under tests/goldens/. The golden can be regenerated by setting
##      CT_PRINT_WRITE_GOLDEN=1 in the environment.


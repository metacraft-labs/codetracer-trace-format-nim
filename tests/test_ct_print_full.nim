proc findEvent(events: JsonNode, predicate: proc(j: JsonNode): bool {.closure.}): JsonNode =
  for ev in events.elems:
    if predicate(ev):
      return ev
  raise newException(AssertionDefect, "event not found")

proc countWhere(events: JsonNode,
    predicate: proc(j: JsonNode): bool {.closure.}): int =
  for ev in events.elems:
    if predicate(ev):
      result += 1

proc test_top_level_shape() =
  let bytes = buildFixtureBytes()
  let readerRes = openNewTraceFromBytes(bytes)
  doAssert readerRes.isOk, readerRes.error
  var reader = readerRes.get()
  let root = buildFullDocument(reader, FullOpts(stripPaths: false))

  doAssert root.hasKey("metadata"), "missing metadata"
  doAssert root.hasKey("paths"), "missing paths"
  doAssert root.hasKey("functions"), "missing functions"
  doAssert root.hasKey("varnames"), "missing varnames"
  doAssert root.hasKey("types"), "missing types"
  doAssert root.hasKey("counts"), "missing counts"
  doAssert root.hasKey("events"), "missing events"

  doAssert root["metadata"]["program"].getStr == "ct_print_demo"
  doAssert root["metadata"]["workdir"].getStr == "/workspace/demo"
  doAssert root["paths"].len == 2
  doAssert root["paths"][0].getStr == "/workspace/demo/main.py"
  doAssert root["paths"][1].getStr == "/workspace/demo/util.py"
  doAssert root["functions"][0].getStr == "main"
  doAssert root["functions"][1].getStr == "compute"
  doAssert root["counts"]["steps"].getInt == 10
  doAssert root["counts"]["calls"].getInt == 2
  doAssert root["counts"]["values"].getInt == 10
  doAssert root["counts"]["io_events"].getInt == 2
  echo "[ok] test_top_level_shape"

